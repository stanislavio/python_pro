
numbers = [1, 2, 3, 4, 5]

index = 0  # індекс поточного елемента в масиві

# ітеруємось в циклі поки індекс поточного елементу не буде дорівнювати довжині списку
while index != len(numbers):
    print(numbers[index])  # виводимо поточний елемент масиву
    index += 1  # переходимо до наступного елементу масиву
